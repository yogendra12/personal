package com.personal.basemodel;

public class BaseChildInfo {
	protected int childId;
	protected String rollNo;
	protected String childName;
	protected String aboutChild;
	protected String childPhotoPath;
	protected String updatedBy;
	protected String updatedDate;

	public int getChildId() {
		return childId;
	}

	public void setChildId(int childId) {
		this.childId = childId;
	}

	public String getRollNo() {
		return rollNo;
	}

	public void setRollNo(String rollNo) {
		this.rollNo = rollNo;
	}

	public String getChildName() {
		return childName;
	}

	public void setChildName(String childName) {
		this.childName = childName;
	}

	public String getAboutChild() {
		return aboutChild;
	}

	public void setAboutChild(String aboutChild) {
		this.aboutChild = aboutChild;
	}

	public String getChildPhotoPath() {
		return childPhotoPath;
	}

	public void setChildPhotoPath(String childPhotoPath) {
		this.childPhotoPath = childPhotoPath;
	}

	public String getUpdatedBy() {
		return updatedBy;
	}

	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}

	public String getUpdatedDate() {
		return updatedDate;
	}

	public void setUpdatedDate(String updatedDate) {
		this.updatedDate = updatedDate;
	}

}
